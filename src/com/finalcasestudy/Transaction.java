package com.finalcasestudy;

public class Transaction{

	
	private int grossWeight;
	private String origin;
	private String destination;
	private int tareWeight;
	private int loadWeight;
	private String content;
	private String transientStart;
	private String transientEnd;
	private String driver;
	private String truckID;
	
	public int getGrossWeight() {
		return grossWeight;
	}
	public void setGrossWeight(int grossWeight) {
		this.grossWeight = grossWeight;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public int getTareWeight() {
		return tareWeight;
	}
	public void setTareWeight(int tareWeight) {
		this.tareWeight = tareWeight;
	}
	public int getLoadWeight() {
		return loadWeight;
	}
	public void setLoadWeight(int loadWeight) {
		this.loadWeight = loadWeight;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTransientStart() {
		return transientStart;
	}
	public void setTransientStart(String transientStart) {
		this.transientStart = transientStart;
	}
	public String getTransientEnd() {
		return transientEnd;
	}
	public void setTransientEnd(String transientEnd) {
		this.transientEnd = transientEnd;
	}
	public String getDriver() {
		return driver;
	}
	public void setDriver(String driver) {
		this.driver = driver;
	}
	public String getTruckID() {
		return truckID;
	}
	public void setTruckID(String truckID) {
		this.truckID = truckID;
	}
	
	
	
	
	
}
